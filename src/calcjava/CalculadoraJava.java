    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        try {
            int v1 = Integer.parseInt(valor1.getText());
            int v2 = Integer.parseInt(valor2.getText());
     
            switch (operador.getText()) {
                case "+":
                    resultado.setText(Integer.toString(Calculo.Adicao(v1, v2)));
                    break;
                case "-":
                    resultado.setText(Integer.toString(Calculo.Subtracao(v1, v2)));
                    break;
                case "x":
                    resultado.setText(Integer.toString(Calculo.Multiplicacao(v1, v2)));
                    break;
                default:
                    resultado.setText(Float.toString(Calculo.Divisao(v1, v2)));
                    break;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_calcularActionPerformed

